# Conversão para matrizes NumPy
# =============================================================================
def dict_to_matrices(bars, lines, generators, wind_generators):
    """
    Converte as listas de dicionários em matrizes NumPy no formato esperado.

    DBAR colunas:
      [barra, tipo, G, vt, angulo, pg, qg, qmin, qmax, pload, qload, qbar]

    DLIN colunas:
      [de, para, r, x, bsh, tap, 0, 0, 0, cap]

    DGER colunas:
      [barra, pgmin, pgmax, custo]

    DGW colunas:
      [barra, pgmin, pgmax, custo]
    """
    DBAR = np.array([
        [b["barra"], b["tipo"], b["G"], b["vt"], b["angulo"],
         b["pg"], b["qg"], b["qmin"], b["qmax"],
         b["pload"], b["qload"], b["qbar"]]
        for b in bars
    ], dtype=float)

    DLIN = np.array([
        [l["de"], l["para"], l["r"], l["x"], l["bsh"],
         l["tap"], 0.0, 0.0, 0.0, l["cap"]]
        for l in lines
    ], dtype=float)

    DGER = np.array([
        [g["barra"], g["pgmin"], g["pgmax"], g["custo"]]
        for g in generators
    ], dtype=float)

    DGW = np.array([
        [w["barra"], w["pgmin"], w["pgmax"], w["custo"]]
        for w in wind_generators
    ], dtype=float)

    return DBAR, DLIN, DGER, DGW


